    }

    @Test("variable.builtin → .keyword (_G, _ENV, self)")
    func variableBuiltin() {
        #expect(CaptureMapping.token(for: "variable.builtin") == .keyword)
    }

    @Test("constant.builtin → .keyword (nil, true, false)")
    func constantBuiltin() {
        #expect(CaptureMapping.token(for: "constant.builtin") == .keyword)
    }

    @Test("label → .keyword (goto labels)")
    func labelCapture() {
        #expect(CaptureMapping.token(for: "label") == .keyword)
    }

    @Test("string → .string")
    func stringCapture() {
        #expect(CaptureMapping.token(for: "string") == .string)
