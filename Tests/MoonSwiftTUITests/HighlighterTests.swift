// Role: Tests for the Highlighter component. Verifies:
//   1. Specific token kinds appear at expected ranges for Lua source.
//   2. LRU cache evicts the least-recently-used entry at capacity without error.
//   3. Incremental re-parse after an edit: spans are produced correctly.
//   4. First-render-unhighlighted contract via the Reducer.
//   5. CaptureMapping produces the correct ThemeToken per ux-spec.md §8.2.
//
// Threading: Highlighter posts results asynchronously on its serial queue.
// Tests poll a real EventChannel using drainAll() inside a retry loop with a
// 5-second deadline — generous enough to tolerate CI thread starvation (80 ms+).
//
// Timing notes: never use fixed sleeps. The retry loop below checks every 10 ms
// and gives up after 5 seconds total.

