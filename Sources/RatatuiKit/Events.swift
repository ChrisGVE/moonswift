// File: Sources/RatatuiKit/Events.swift
// Role: Decodes raw RffiEvent structs from the CRatatuiFFI poll call into the
//       Swift Event enum consumed by MoonSwiftTUI's EventPump. All decoding
//       is pure — the EventPump calls poll(timeout:) then hands the result up.
// Upstream: CRatatuiFFI (rffi_poll_event, RffiEvent, RffiEvent field constants)
// Downstream: MoonSwiftTUI/App/EventPump.swift (calls poll, receives Event)

import CRatatuiFFI
import Foundation

// MARK: - KeyCode

/// A decoded key code from a terminal key event.
///
/// `.char(Unicode.Scalar)` carries the Unicode scalar for printable characters
/// (e.g. `'a'`, `'A'`, `'€'`). Named keys cover the special-key vocabulary
/// of crossterm's `KeyCode` enum; `unknown` preserves any code not yet
/// mapped so the pump never silently drops input.
public enum KeyCode: Sendable, Equatable {
    case char(Unicode.Scalar)
    case backspace
    case enter
    case left
    case right
    case up
    case down
    case home
    case end
    case pageUp
    case pageDown
    case tab
    case backTab
    case delete
    case insert
    case f(UInt8)            // F1–F12; value is the function-key number
    case escape
    case capsLock
    case scrollLock
    case numLock
    case printScreen
    case pause
    case menu
    case null
    case unknown(UInt32)     // preserves unmapped codes for forward compat
}

// MARK: - KeyModifiers

/// Modifier keys active during a key event.
///
/// This is a Swift `OptionSet` matching the shim's `key_mods` bitfield:
/// `SHIFT = 1`, `ALT = 2`, `CTRL = 4` (values from the generated header).
public struct KeyModifiers: OptionSet, Sendable {
    public let rawValue: UInt8

    public init(rawValue: UInt8) { self.rawValue = rawValue }

    /// The Shift key.
    public static let shift = KeyModifiers(rawValue: 1)
    /// The Alt / Option key.
    public static let alt   = KeyModifiers(rawValue: 2)
    /// The Control key.
    public static let ctrl  = KeyModifiers(rawValue: 4)
}

// MARK: - MouseKind / MouseButton

/// The kind of mouse event received.
public enum MouseKind: UInt32, Sendable {
    case down       = 0
    case up         = 1
    case drag       = 2
    case moved      = 3
    case scrollDown = 4
    case scrollUp   = 5
    case scrollLeft = 6
    case scrollRight = 7
}

/// The mouse button associated with a mouse event.
public enum MouseButton: UInt32, Sendable {
    case left   = 0
    case right  = 1
    case middle = 2
    case none   = 3
}

// MARK: - Event

/// A decoded terminal event, ready for the EventPump to post as an AppEvent.
///
/// `Event` maps one-to-one with `RffiEvent` fields; no information is lost.
/// The EventPump decodes via `Event(from:)` and forwards to the EventChannel.
public enum Event: Sendable {
    /// A key was pressed.
    case key(KeyCode, modifiers: KeyModifiers)
    /// The terminal was resized to the given dimensions (columns × rows).
    case resize(cols: UInt16, rows: UInt16)
    /// A mouse event occurred at the given cell position.
    case mouse(
        kind: MouseKind,
        button: MouseButton,
        col: UInt16,
        row: UInt16,
        modifiers: KeyModifiers
    )
    /// Bracketed-paste content received.
    case paste(String)
}

// MARK: - RffiEvent discriminant constants

// Mirror the numeric discriminants the Rust side writes into `RffiEvent.kind`.
// Verified against ratatui_ffi.h / events.rs — these are compile-time constants
// in the generated header, so a mismatch is a build error, not a silent bug.
private enum RffiEventKind: UInt32 {
    case key    = 0
    case resize = 1
    case mouse  = 2
    case paste  = 3
}

