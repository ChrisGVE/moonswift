    case enter       = 1
    case left        = 2
    case right       = 3
    case up          = 4
    case down        = 5
    case home        = 6
    case end         = 7
    case pageUp      = 8
    case pageDown    = 9
    case tab         = 10
    case backTab     = 11
    case delete      = 12
    case insert      = 13
    case f1          = 14
    case f2          = 15
    case f3          = 16
    case f4          = 17
    case f5          = 18
    case f6          = 19
    case f7          = 20
    case f8          = 21
    case f9          = 22
    case f10         = 23
    case f11         = 24
